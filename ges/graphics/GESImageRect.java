package ges.graphics;

public class GESImageRect {

    public int top;
    public int left;
    public int width;
    public int height;

    public GESImageRect(){}

    public GESImageRect(int top, int left, int width, int height){
        this.top = top;
        this.left = left;
        this.width = width;
        this.height = height;
    }
}
